export const CATEGORY_CLASSIFICATION_PROMPT = `أنت خبير تقني في تصنيف قطع غيار السيارات (خصوصاً في السوق الخليجي واليمني).
مهمتك هي قراءة اسم المنتج وتحديد ما إذا كان يطابق أحد الأقسام الموجودة في القائمة المرقمة أم يحتاج لقسم جديد.

⚠️ قواعد التفكير (Reasoning):
1. الكلمة الأولى غالباً هي "الماركة" أو "النوع" (ارماس، ابوال، كيا)، ركز على الكلمة التي تليها لتحديد "ماهية" القطعة (مسمار، صليب، سلف، دينمة).
2. "ابوال مسمار" يندرج تحت "مسامير" أو "مسامير كفرات".
3. إذا وجدت تطابقاً قوياً مع قائمة الأرقام المقدمة، استخدم "category_index".
4. إذا لم تجد تطابقاً (حتى في الجمع والمفرد)، اقترح اسماً دقيقاً للقسم في حقل "suggested_category_name" واجعل "is_new_category" هي true.
5. ⛔ تحذير: لا تصنف كل شيء تحت قسم واحد عشوائي. إذا لم تكن متأكداً بنسبة 90%، فعليك اقتراح "قسم جديد".

أمثلة للتوضيح:
- "ابوال مسمار كفر" -> الفئة هي "مسامير كفرات" (قسم جديد لو لم يوجد رقم له).
- "ارميش سلف دينة" -> الفئة هي "قطع سلف" (رقمها من القائمة لو وجدت).
- "ارماس هواية النترا" -> الفئة هي "هوايات مكينة" (قسم جديد).

الهيكل المطلوب (Strict JSON):
[
  {
    "product_id": "معرف المنتج",
    "category_index": "رقم القسم من القائمة (فقط إذا كان موجوداً)",
    "suggested_category_name": "اسم القسم (إلزامي إذا كان جديداً، اختياري إذا كان موجوداً للتأكيد)",
    "is_new_category": boolean,
    "confidence": 0.0-1.0,
    "reasoning": "لماذا اخترت هذا؟ (مثلاً: تطابق كلمة 'سلف')"
  }
]`;

export const buildCategorizationPrompt = (products: {id: string, name: string}[], categories: {id: string, name: string}[]) => {
    return `
--- قائمة الأقسام الحالية (اختر الرقم المناسب) ---
${categories.map((c, i) => `${i + 1}. ${c.name}`).join('\n')}

--- المنتجات المطلوب فحصها (15 منتج) ---
${products.map(p => `- [${p.id}] ${p.name}`).join('\n')}

المطلوب: تصنيف دقيق جداً لكل منتج.
`;
};
